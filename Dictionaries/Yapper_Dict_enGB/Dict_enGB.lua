YapperAPI:RegisterDictionary("enGB", function()
    local d = {
        extends = "enBase",
        isDelta = true,
        words = {},
        phonetics = {},
    }
    --- Morphology fallback: check if word is a common inflected form of a known base.
                d.GetAffixStrippedWord = function(self, word)
                    -- 1. Plurals (-s)
                    if word:sub(-1) == "s" then
                        local root = word:sub(1, -2)
                        if self:Contains(root) then return root end
                    end
        
                    -- 2. Verbs (-ing, -ed)
                    local root = word:match("^(.+)ing$") or word:match("^(.+)ed$")
                    if root then
                        -- Case A: No change (talk -> talking)
                        if self:Contains(root) then return root end
                        -- Case B: e-drop (necrotise -> necrotising)
                        local rE = root .. "e"
                        if self:Contains(rE) then return rE end
                    end
        
                    return nil
                end
    
    local tinsert = table.insert
    for _, w in ipairs(getWords_0()) do tinsert(d.words, w) end
    for h, m in pairs(getPhonetics_0()) do d.phonetics[h] = m end
    return d
end)
